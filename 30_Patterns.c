#include <stdio.h>

int main()
{
    int x = 5;

    //________________________________________________________

    for (int i = 1; i <= x; i++)
    {
        for (int j = 1; j <= i; j++)
        {
            printf("%d", j);
        }
        printf("\n");
    }

    // 1
    // 12
    // 123
    // 1234
    // 12345

    //________________________________________________________

    for (int i = x; i >= 1; i--)
    {
        for (int j = 1; j <= i; j++)
        {
            printf("%d", j);
        }
        printf("\n");
    }

    // 12345
    // 1234
    // 123
    // 12
    // 1

    //________________________________________________________

    for (int i = x; i >= 1; i--)
    {
        for (int j = i; j >= 1; j--)
        {
            printf("%d", i);
        }
        printf("\n");
    }
    // 55555
    // 4444
    // 333
    // 22
    // 1

    //________________________________________________________

    for (int i = 1; i <= x; i++)
    {
        for (int j = 1; j <= i; j++)
        {
            printf("%d", i);
        }
        printf("\n");
    }

    // 1
    // 22
    // 333
    // 4444
    // 55555

    //________________________________________________________

    return 0;
}